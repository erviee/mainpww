function myFunction() {
    let a = 100;
    let b = 50;
    let x = a + b;
    document.getElementById('demo').innerHTML = x;
    document.getElementById("demo").style.fontSize = "15px";
    document.getElementById("demo").style.padding = "12px";
    document.getElementById("demo").style.backgroundColor = "cadetblue";
    document.getElementById('demo').style.width = "10%";
    document.getElementById('demo').style.borderRadius = "15px";
}

